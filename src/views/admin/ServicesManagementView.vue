const toggleServiceStatus = async (service: Service) => {
  // –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –∫–ª–∏–∫–∏ –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π —É—Å–ª—É–≥–∏
  if (toggleLoading.value.has(service.id)) {
    console.log('‚è≥ Already processing service', service.id)
    return
  }
  
  console.log('üîÑ Toggling status for service:', service.name, 'ID:', service.id, 'Current:', service.is_active)
  
  toggleLoading.value.add(service.id)
  
  try {
    // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –Ω–æ–≤—ã–π —Å—Ç–∞—Ç—É—Å –∑–∞—Ä–∞–Ω–µ–µ
    const newStatus = !service.is_active
    
    console.log('üì§ Sending PUT request with data:', { is_active: newStatus })
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å
    const response = await api.put(`/services/${service.id}`, {
      is_active: newStatus
    })
    
    console.log('üì• API response status:', response.status)
    console.log('üì• API response data:', response.data)
    
    if (response.data && response.data.success) {
      const updatedService = response.data.data
      console.log('‚úÖ Service updated successfully. New status:', updatedService.is_active)
      
      // –ü–µ—Ä–µ–∑–∞–≥—Ä—É–∂–∞–µ–º –≤—Å–µ –¥–∞–Ω–Ω—ã–µ —Å —Å–µ—Ä–≤–µ—Ä–∞ –¥–ª—è –≥–∞—Ä–∞–Ω—Ç–∏–∏ –∫–æ–Ω—Å–∏—Å—Ç–µ–Ω—Ç–Ω–æ—Å—Ç–∏
      console.log('üîÑ Reloading all services from server...')
      await loadServices()
      
      showNotification(
        `–£—Å–ª—É–≥–∞ "${service.name}" ${updatedService.is_active ? '–∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–∞' : '–¥–µ–∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–∞'}`, 
        'success'
      )
    } else {
      throw new Error('API returned unsuccessful response')
    }
    
  } catch (error: any) {
    console.error('‚ùå Error toggling service status:', error)
    console.error('‚ùå Error details:', error.response?.data)
    showNotification('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Å—Ç–∞—Ç—É—Å–∞ —É—Å–ª—É–≥–∏', 'error')
  } finally {
    toggleLoading.value.delete(service.id)
  }
}

